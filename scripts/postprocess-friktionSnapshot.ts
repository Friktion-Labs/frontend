// Loop through all the friktionSnapshot.allMainnetVolts and add them to the staticData.staticInfoByGlobalId. We also validate that all the keys in exampleStaticInfo are present.
for (const globalId of friktionSnapshot.allMainnetVolts) {
  const staticInfo: Record<string, JSONValue> = {};
  for (const key in exampleStaticInfo) {
    if (globalId[key] === undefined) {
      throw new Error(
        `Missing ${key} in friktionSnapshot.allMainnetVolts for ${globalId}`
      );
    }
    staticInfo[key] = globalId[key];
  }
  staticData.staticInfoByGlobalId[globalId.globalId] = staticInfo;
}

